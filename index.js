// Print array index values
let arr = ["mango", 2, "apple", "toothpaste", 45, false];

arr.push(true); // Adds 'true' to the end of the array
arr.push(200); // Adds 200 to the end of the array

// Modify the second element of the array
arr[1] = false;

// Use pop to remove the last element of the array
arr.pop();

